let name, age
name = 'Noel'
age = 23

console.log('o ' + name + ' tem ' + age + ' anos.') //formatando texto com variável utilizando concatenação, texto dentro de aspas e com sinal de soma juntamos o texto com a variável

console.log(`O ${name} tem ${age} anos.`) //formatando texto com variável utilizando a interpolação, utiliza-se crase, variáveis são embrulhadas com cifrão e chaves(template literal or template strings)