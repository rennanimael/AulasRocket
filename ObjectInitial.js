
/*objetos possui propriedades e funcionalidades
suas propriedades podem ser ditas como características do objeto, por exemplo uma pessoa tem nome, idade, cpf, cor do cabelo, etc.
suas funcionalidades são ações que podem ser realizadas por essa pessoa, como programar, andar, limpar a casa entre outros */
